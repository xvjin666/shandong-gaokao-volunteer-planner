def normalize_school_name(name: str) -> str:
    text = str(name or "").strip()
    return SCHOOL_ALIASES.get(text, text)


def school_meta(name: str) -> tuple[str, tuple[str, ...]]:
    normalized = normalize_school_name(name)
    return SCHOOL_META.get(normalized, ("", ()))


def school_tags(record: AdmissionRecord) -> tuple[str, ...]:
    _, mapped_tags = school_meta(record.school_name)
    values: list[str] = []
    for item in (*mapped_tags, record.school_level, *record.tags):
        if item and item not in values:
            values.append(item)
    return tuple(values)


def is_public_undergraduate(record: AdmissionRecord) -> bool:
    school_type = str(record.school_type or "")
    school_name = normalize_school_name(record.school_name)
    if "民办" in school_type or school_name in PRIVATE_UNDERGRADUATE_SCHOOLS:
        return False
    if "独立学院" in school_type:
        return False
    return True
